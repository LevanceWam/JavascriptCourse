// before ES6 we used var there are some issues that we will get into later.
// With ES6 we now use let
// by default variables we define are undefined in the beginning
let name = 'Vance';
console.log(name);

// We have a few rules for naming variables
// Cannot be a reserved keyword
// Should be meaningful
// Cannot start with number (1name)
// cannot contain a space or hypen
// Are case-sensitive
// should use camelcasing

let firstName = 'Levance';
let lastName = 'Wamley';
console.log(firstName, lastName);