// Arithmetic Operators are used to performing calculations.


let x = 10;
let y = 4;

console.log(x + y) // x and y here is what we refer to as an experession.
console.log(x - y)
console.log(x * y)
console.log(x / y)
console.log(x % y)
console.log(x ** y)

// Increment (++)
console.log(++x) // will go up 1 when in front of the variable.

console.log(x++) // when we put it after the x. It will see the x first and print that value we defined up top
console.log(x) //now that we called it. x will go up by 1

// Decrement (--)
console.log(--x) // So when we call the operator first then it will run the operator first. 