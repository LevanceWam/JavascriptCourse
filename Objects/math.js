// Let's take a look at some of the built in objects in JS
// first up is math
// we can go to https://www.w3schools.com/js/js_math.asp
// to learn more of Math obj

// we get a new random number between 0 and 1
console.log(Math.random());

// round
console.log(Math.round(1.4));

// Max returns the biggest number in list
console.log(Math.max(1, 2, 3, 4, 5, 6, 7));

// Min returns smallest number
console.log(Math.min(1, 2, 3, 4, 5, 6));